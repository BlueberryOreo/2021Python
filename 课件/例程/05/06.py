#格式化字符串

print("He has %d dollars." % 10)
print("He has %f dollars." % 10)
print("He has %.2f dollars." % 10)
print("He has %8.2f dollars." % 10)
print("He has %-8.2f dollars." % 10)
print()

print("He has %i dollars." % 0o10)
print("He has %d dollars." % 0b10)
print()

print("He paid %.2f dollars for %d apples." % (5, 3))
